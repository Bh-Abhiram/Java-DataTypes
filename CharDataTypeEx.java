class CharDataTypeEx
{
	public static void main(String args[])
	{
		char ch1, ch2;
		ch1= 'A';
		System.out.println("Char 1 is : "+ch1);
		ch1++;
		System.out.println("incremented Char 1 is : "+ch1);
		ch2= 'C';
		System.out.println(ch1 + ch2);
		System.out.println(String.valueOf(ch1) + String.valueOf(ch2));

	}
}